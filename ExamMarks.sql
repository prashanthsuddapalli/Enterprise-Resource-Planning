CREATE TABLE class_1 (
    student_id text PRIMARY KEY,
    student_name text NOT NULL,
    telugu_finaltest real,
    hindi_finaltest real,
    english_finaltest real,
    telugu_unittest real,
    hindi_unittest real,
    english_unittest real,
    CHECK (telugu_finaltest >= 0.0 AND telugu_finaltest <= 100.0),
    CHECK (hindi_finaltest >= 0.0 AND hindi_finaltest <= 100.0),
    CHECK (english_finaltest >= 0.0 AND english_finaltest <= 100.0),
    CHECK (telugu_unittest >= 0.0 AND telugu_unittest <= 25.0),
    CHECK (hindi_unittest >= 0.0 AND hindi_unittest <= 25.0),
    CHECK (english_unittest >= 0.0 AND english_unittest <= 25.0)
);

